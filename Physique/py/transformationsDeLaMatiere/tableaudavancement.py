'''
Tableau d'avancement = représentation utilisée pour étudier les réactions chimiques.
Il décrit l'évolution des quantités de matière d'un système chimique de l'état initial à l'état final, en fonction de l'avancement x x x.

Pourquoi un tableau d'avancement?

Avancement d'une reaction = lors d'une réaction chimique, la quantité de matière de chaque reactif va diminuer au cours de la réaction.
On parle d'avancement le suivi de cette évolution. 
Grandeur notee x et exprimee en mol.

réactif limitant = réactif totalement consommé dans l'état final

état initial = avant que la réaction commence

état intermédiaire = au cours de la réaction

état final = lorsqu'un des réactifs est totalement consommé ou en quantité non suffisante

- si xfinal = xmax alors la réaction est totale,
– si xfinal < xmax alors la réaction n’est pas totale
'''