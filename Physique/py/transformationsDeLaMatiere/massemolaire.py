'''
Masse molaire atomique = masse d'une mole d'atomes de ce dit élément chimique, mesurée alors en g/mol-1

Masse molaire moléculaire = somme des masses molaires atomiques des éléments chimiques constituant la molécule. Mesurée en g.mol-1
EXEMPLE:
Masse molaire atomique de l'oxygène O = 16.0 g.mol-1
M(H2O) = M(H)*2 + M(O) = 2*1.00 + 16 = 18 g.mol-1

M = masse molaire de l'échantillon (g.mol-1)
m = masse de l'échantillon (g)
n = quantité de matière de l'échantillon (mol)
'''